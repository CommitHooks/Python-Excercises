# end keyword: default is to add a new line
print('Hello')
print('World')

# end keyword: end = '' (empty space)
print('Hello', end='')
print('World')

# sep keyword: default is to add a single space when there are multiple parameters
print('cats', 'dogs', 'mice')

# sep keyword: sep = ', '
print('cats', 'dogs', 'mice', sep=', ') 
